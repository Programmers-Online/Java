public class RelationOperator {
   public static void main(String[] args){

      int a = 6;
      int b = 7;

// ( == ) => Equal to 
// ( != ) => Not Equal to
// ( < ) => Less than
// ( > ) => Greater than
// ( <= ) => Less than or equal to
// ( >= ) => Greater than or equal to

      System.out.println(" a == b " + (a == b));
      System.out.println(" a != b " + (a != b));

      System.out.println(" a < b " + (a < b));
      System.out.println(" a > b " + (a > b));

      System.out.println(" a <= b " + (a <= b));
      System.out.println(" a >= b " + (a >= b));
   }
}