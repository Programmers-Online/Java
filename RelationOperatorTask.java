public class RelationOperatorTask {

   public static void main(String[] args) {

      int a = 8;
      int b = 9;

      System.out.println("a == b => " + (a == b));
      System.out.println("a != b => " + (a != b));
      System.out.println("a < b => " + (a < b));
      System.out.println("a > b => " + (a > b));
      System.out.println("a <= b => " + (a <= b));
      System.out.println("a >= b => " + (a >= b));

   }
}